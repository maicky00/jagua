    private void btnAsistenciaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAsistenciaActionPerformed
        try {
            if (!txtId.getText().equals("")) {
                FrmAsistenciaPesillo as = new FrmAsistenciaPesillo();
                int n = jTable1.getSelectedRow();
                int mes = jMonthChooser1.getMonth() + 1;
                int anio = jYearChooser1.getYear();

                cf = new ControlFormularios();

                FrmAsistenciaPesillo.lblIdPlan.setText(txtId.getText());
                FrmAsistenciaPesillo.txtValor.setText(jTable1.getValueAt(n, 4).toString());
                FrmAsistenciaPesillo.txtMes.setText(mes + "");
                FrmAsistenciaPesillo.txtAnio.setText(""+anio);
                FrmAsistenciaPesillo.txtTipo.setText(jTable1.getValueAt(n, 1).toString());
                this.dispose();
                cf.ControlaInstancia(as);
                cap.cargarTablaAsistenciaPesillo(FrmAsistenciaPesillo.tab, Integer.parseInt(txtId.getText()));
                cap.cargarTablaMedidor2(FrmAsistenciaPesillo.tab1,Integer.valueOf(txtId.getText()));
            } else {
                JOptionPane.showMessageDialog(null, "Seleccione un Evento!.",
                "Error de Proceso",
                JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {

        }
    }//GEN-LAST:event_btnAsistenciaActionPerformed

