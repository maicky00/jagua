    private void inicio() {
        Dimension desktopSize = FrmPrincipal.jDesktopPane1.getSize();
        Dimension jInternalFrameSize = this.getSize();
        this.setLocation((desktopSize.width - jInternalFrameSize.width) / 2, 4);
        ta = new classTarifassobrante();
        
        Tarifassobrante tarifa=new Tarifassobrante();
        tarifa=ta.buscarIdTarifassobrante(1);
        
        txtIdTarifas.setText(String.valueOf(tarifa.getIdtarifassobrante()));
        txtTarifaMensual.setText(String.valueOf(tarifa.getTarifamensual()));
        txtDescripcion.setText(String.valueOf(tarifa.getDescripcion()));
        txtIva.setText(String.valueOf(tarifa.getIva()));
    }
    
    private void mostrar() {
        txtTarifaMensual.setEditable(true);
        txtDescripcion.setEditable(true);
        txtDescripcion.setEnabled(true);
        txtIva.setEnabled(true);
        txtIva.setEditable(true);
        btnGuardar.setEnabled(true);
        btnCancelar.setEnabled(true);
        btnEditar.setEnabled(false);
    }
    
    private void ocultar() {
        txtTarifaMensual.setEditable(false);
        txtDescripcion.setEditable(false);
        txtIva.setEditable(false);
        btnGuardar.setEnabled(false);
        btnCancelar.setEnabled(false);
        btnEditar.setEnabled(true);
        
    }

