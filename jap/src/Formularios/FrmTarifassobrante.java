    private void txtTarifaMensualKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtTarifaMensualKeyTyped
        char c = evt.getKeyChar();
        
        if (Character.isLetter(c)) {
            getToolkit().beep();
            
            evt.consume();
            
            mensaje.setText("error de ingreso, ingrese digitos");
        } else {
            mensaje.setText("");
        }        // TODO add your handling code here:
    }//GEN-LAST:event_txtTarifaMensualKeyTyped

